    public void ordenarVentanas()
    {
        if(archivos.cargarArchivo())
        {
            controlador.mostrarVentanaLogin();
        }
        else
        {
            JOptionPane.showMessageDialog(null, "No hay ning√∫n usuario registrado en el sistema.\nDebe crear un nuevo usuario antes de continuar.");
            controlador.crearPrimerUsuario();
        }
    }
    
    public void agregarControlador(CNTRL_MenuPrincipal controlador)
    {
        jmi_Usuarios.addActionListener(controlador);
    }
    
